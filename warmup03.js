/**
 * 1-write a function that returns the product of any number by 3.
 */
// Function Declaration
function multiplyByThreeDeclaration(number) {
  return number * 3;
}

// Function Expression
let multiplyByThreeExpression = function(number) {
  return number * 3;
};

// Arrow Function
let multiplyByThreeArrow = number => number * 3;


